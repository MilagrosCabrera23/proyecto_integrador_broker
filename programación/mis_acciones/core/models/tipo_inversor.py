class Tipo_Inversor: 
    def __init__(self, id_tipo, descripcion): 
        self.__id_tipo = id_tipo
        self.__descripcion = descripcion

def tipo_inversor(self):
    return self.__id_tipo

def descripcion(self): 
    return self.__descripcion

def __str__(self): 
    return f"Tipo de inversor: {self.id_tipo} -  {self.descripcion}"

